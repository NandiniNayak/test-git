def sum(num1,num2)
  sum = num1 + num2
  return sum
end

result = sum(4,4)
puts "sum of two number is #{result}"

def subtract(num1,num2)
  diff = num1 - num2
  return diff
end

result = subtract(4,4)
puts "difference of two numbers are #{result}"

def product(num1,num2)
  product = num1 * num2
  return product
end

result = product(4,4)
puts "product of two numbers are #{result}"
